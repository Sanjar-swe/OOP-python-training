import math
class Rectangle: #–ø—Ä—è–º–æ—É–≥–æ–ª—å–Ω–∏–∫
    def __init__(self, length: int, width: int):
        self.length = length
        self.width = width
        
    def area(self):
        return self.length * self.width
    
class Circle: #–∫—Ä—É–≥
    def __init__(self, radius: int):
        self.radius = radius
        
    def area(self):
        return math.pi * self.radius ** 2
    
    
rect = Rectangle(5, 3)
circle = Circle(3)
print(f"Ploshad Rectangle {rect.area()} metr")
print(f"Ploshad Circle {circle.area()} metr")

