# **Задача 1: "Создание простого класса"**  
# Создай класс `Dog`, у которого есть:
# - атрибуты (переменные внутри объекта): `name`, `age`
# - метод (функция внутри объекта): `bark`, который печатает "Гав-гав!"

# **Зачем:** Чтобы ты понял, что класс — это **чертеж** для объектов (например, шаблон для собаки).

# ---

class Dog:
    name: str
    age: int
    def bark(self): 
        print("Гав-гав!")
        
dog = Dog()
dog.name = "Барсик"
dog.age = 3
dog.bark()